// Get page content by page name
function getPageContent($page) {
  global $pageTexts;
  return isset($pageTexts[$page]) ? $pageTexts[$page] : $pageTexts['homepage'];
}

// Display text container for a page
function displayTextContainer($page) {
  $content = getPageContent($page);
  
  if (isset($content['main_content']) && is_array($content['main_content'])) {
    echo '<section class="text-container">';
    
    foreach ($content['main_content'] as $section) {
      echo '<div class="text-box">';
      
      if (!empty($section['title'])) {
        echo '<h2 class="text-title">' . $section['title'] . '</h2>';
      }
      
      echo '<p class="text-content">' . $section['content'] . '</p>';
      echo '</div>';
    }
    
    echo '</section>';
  }
}
